

 
 

public class Student
{
    String name;
    String id;
    String adress;
    Double cgpa;
    public void setName(String n)
    {
        name=n;
    }
    public String getName()
    {
        return name;
    }
    public void setId(String i)
    {
        id=i;
    }
    public String getId()
    {
        return id;
    }
    public void setAdress(String a)
    {
        adress=a;
    }
    public String getAdress()
    {
        return adress;
    }
    public void setCgpa(Double c)
    {
        cgpa=c;
    }
    public double getCgpa()
    {
        return cgpa;
    }
}


