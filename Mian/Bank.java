public class Bank
{
  String name;
  String address;
  String Id;
Double balance;
    public void setName(String n)
    {
        name=n;
    }
    public String getName()
    {
        return name;
    }
    public void setId(String i)
    {
        Id=i;
    }
    public String getId()
    {
        return Id;
    }
    public void setAddress(String a)
    {
        address=a;
    }
    public String getAddress()
    {
        return address;
    }
    public void setBalance(Double B)
    {
        balance=B;
    }
    public double getBalance()
    {
        return balance;
    }
}
